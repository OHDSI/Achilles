#' Create indices on Achilles results tables and concept hierarchy
#'
#' @description
#' \code{createIndices} executes script to create indicies on the Achilles tables.
#'
#' @details
#' \code{createIndices} executes script to create indicies on the Achilles tables.
#' 
#' @param connectionDetails  An R object of type ConnectionDetail (details for the function that contains server info, database type, optionally username/password, port)
#' @param resultsDatabaseSchema		string name of database schema that holds the results tables for indexing. On SQL Server, this should specifiy both the database and the schema, so for example 'results.dbo'.
#' @param oracleTempSchema    For Oracle only: the name of the database schema where you want all temporary tables to be managed. Requires create/insert permissions to this database. 
#' @param sqlOnly     if TRUE, only the SQL code will be generated
#' @param cdmVersion     Define the OMOP CDM version used:  currently support only "5".  Default = "5"
#' 
#' @return nothing is returned
#' @examples \dontrun{
#'   connectionDetails <- DatabaseConnector::createConnectionDetails(dbms="sql server", server="myserver")
#'   createIndices <- createIndices(connectionDetails, resultsDatabaseSchema="scratch")
#' }
#' @export
createIndices <- function (connectionDetails, 
                           resultsDatabaseSchema,
                           oracleTempSchema = resultsDatabaseSchema,
                           sqlOnly = FALSE,
                           cdmVersion = "5"){
  
  if (cdmVersion == "5") {
    sqlFile = "Achilles_indices_v5.sql"
  } else  {
    stop("Error: Invalid CDM Version number, only version 5 supported")
  }
  
  if (connectionDetails$dbms=='redshift') {
    stop("Error: RedShift does not support creating indices")
  }
  
  is_pdw <- (connectionDetails$dbms == "pdw")
  
  indicesSql <- SqlRender::loadRenderTranslateSql(sqlFilename = sqlFile,
                                         packageName = "Achilles",
                                         dbms = connectionDetails$dbms,
                                         oracleTempSchema = oracleTempSchema,
                                         is_pdw = is_pdw,
                                         results_database_schema = resultsDatabaseSchema);
  
  if (!sqlOnly) {
    conn <- DatabaseConnector::connect(connectionDetails);
    writeLines("Executing indices creation. This could take a while")
    DatabaseConnector::executeSql(conn,indicesSql)
    writeLines(paste("Done. Indices created in",resultsDatabaseSchema))  
  }
  return(indicesSql)
}

